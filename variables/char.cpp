// Function to print character variables

#include <iostream>
using namespace std;

int main()
{
    // declaring character variables
    char a = 'A';
    char dollar = '$';
    char hastag = '#';

    // printing character variables
    cout << "Character a: " << a << endl;
    cout << "Character dollar: " << dollar << endl;
    cout << "Character hastag: " << hastag << endl;
}
